// OCL_CHECK doesn't work if call has templatized function call
#define OCL_CHECK(error, call)  \
    call;  \
    if (error != CL_SUCCESS) {  \
        fprintf(stderr,  \
            "%s:%d Error calling " #call ", error code is: %d\n",  \
            __FILE__, __LINE__, error);  \
        exit(EXIT_FAILURE);  \
    }

